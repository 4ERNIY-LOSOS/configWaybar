#!/bin/bash

# Убиваем все существующие экземпляры Waybar
# Используем killall для большей надежности, если pkill -x не сработает или есть несколько процессов
# killall -q waybar # -q подавляет сообщения об ошибках, если процесс не найден
pkill -x waybar

# Небольшая пауза, чтобы Waybar успел полностью завершиться
sleep 0.2

# Запускаем Waybar снова
# Предполагается, что waybar находится в вашем $PATH
# Если Waybar запускается с нестандартными путями к конфигу/стилям, их нужно указать здесь.
# Например: waybar -c ~/.config/waybar/config -s ~/.config/waybar/style.css &
# Для текущей конфигурации достаточно просто waybar &
waybar &
